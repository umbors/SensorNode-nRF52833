void psensor_data_send(uint8_t dataToSend[]){
		ret_code_t err_code;		
		err_code = ble_lbs_on_psensor_change(m_conn_handle, &m_lbs, dataToSend);
    if (err_code != NRF_SUCCESS &&
			err_code != BLE_ERROR_INVALID_CONN_HANDLE &&
			err_code != NRF_ERROR_INVALID_STATE &&
			err_code != BLE_ERROR_GATTS_SYS_ATTR_MISSING)
		{
			APP_ERROR_CHECK(err_code);
		}
}
void psensor_event_handler(nrfx_saadc_evt_t const * p_event)
{
		//send twice,add buffer and calculate if the power consumption is too high
    if (p_event->type == NRFX_SAADC_EVT_DONE)
    {
			uint8_t index = 10*psensor_col + 2*psensor_row;
      for(int i = 0; i < p_event->data.done.size; i++)
      {
				//NRF_LOG_INFO("CH%d: %d", i, p_event->data.done.p_buffer[i]);
				dataToSend[i + index] = (uint8_t)(p_event->data.done.p_buffer[i]>>8);
				dataToSend[i + index + 1] = (uint8_t)p_event->data.done.p_buffer[i];
      }
			//send data when scan is done
			if(psensor_col == 3 && psensor_row == 4){
				psensor_data_send(dataToSend);
				psensor_col = 0;
				psensor_row = 0;
			}
			else if(psensor_row == 4){
				psensor_col += 1;
				psensor_row = 0;
			}
			else{
				psensor_row ++;
			}
        adc_is_ready = true;
    }

    /* Applying workaround from Errata 212, otherwise current is stuck at 4-500uA during sleep after first sample. */
    volatile uint32_t temp1;
    volatile uint32_t temp2;
    volatile uint32_t temp3;

    temp1 = *(volatile uint32_t *)0x40007640ul;
    temp2 = *(volatile uint32_t *)0x40007644ul;
    temp3 = *(volatile uint32_t *)0x40007648ul;

    *(volatile uint32_t *)0x40007FFCul = 0ul; 
    *(volatile uint32_t *)0x40007FFCul; 
    *(volatile uint32_t *)0x40007FFCul = 1ul;

    *(volatile uint32_t *)0x40007640ul = temp1;
    *(volatile uint32_t *)0x40007644ul = temp2;
    *(volatile uint32_t *)0x40007648ul = temp3;
}

void ble_evt_handler(ble_evt_t const * p_ble_evt, void * p_context)
{
    ret_code_t err_code;

    switch (p_ble_evt->header.evt_id)
    {
        case BLE_GAP_EVT_CONNECTED:
            NRF_LOG_INFO("Connected");
            m_conn_handle = p_ble_evt->evt.gap_evt.conn_handle;
            err_code = nrf_ble_qwr_conn_handle_assign(&m_qwr, m_conn_handle);
            APP_ERROR_CHECK(err_code);
						
						psensor_start();
						break;

        case BLE_GAP_EVT_DISCONNECTED:
            NRF_LOG_INFO("Disconnected");
            m_conn_handle = BLE_CONN_HANDLE_INVALID;
						psensor_stop();
            advertising_start();
            break;

        case BLE_GAP_EVT_SEC_PARAMS_REQUEST:
            // Pairing not supported
            err_code = sd_ble_gap_sec_params_reply(m_conn_handle,
                                                   BLE_GAP_SEC_STATUS_PAIRING_NOT_SUPP,
                                                   NULL,
                                                   NULL);
            APP_ERROR_CHECK(err_code);
            break;

        case BLE_GAP_EVT_PHY_UPDATE_REQUEST:
        {
            NRF_LOG_DEBUG("PHY update request.");
            ble_gap_phys_t const phys =
            {
                .rx_phys = BLE_GAP_PHY_AUTO,
                .tx_phys = BLE_GAP_PHY_AUTO,
            };
            err_code = sd_ble_gap_phy_update(p_ble_evt->evt.gap_evt.conn_handle, &phys);
            APP_ERROR_CHECK(err_code);
        } break;

        case BLE_GATTS_EVT_SYS_ATTR_MISSING:
            // No system attributes have been stored.
            err_code = sd_ble_gatts_sys_attr_set(m_conn_handle, NULL, 0, 0);
            APP_ERROR_CHECK(err_code);
            break;

        case BLE_GATTC_EVT_TIMEOUT:
            // Disconnect on GATT Client timeout event.
            NRF_LOG_DEBUG("GATT Client Timeout.");
            err_code = sd_ble_gap_disconnect(p_ble_evt->evt.gattc_evt.conn_handle,
                                             BLE_HCI_REMOTE_USER_TERMINATED_CONNECTION);
            APP_ERROR_CHECK(err_code);
            break;

        case BLE_GATTS_EVT_TIMEOUT:
            // Disconnect on GATT Server timeout event.
            NRF_LOG_DEBUG("GATT Server Timeout.");
            err_code = sd_ble_gap_disconnect(p_ble_evt->evt.gatts_evt.conn_handle,
                                             BLE_HCI_REMOTE_USER_TERMINATED_CONNECTION);
            APP_ERROR_CHECK(err_code);
            break;

        default:
            // No implementation needed.
            break;
    }
}

